public class Estudiante {
    private String nombre;
    private int edad;
    private String carrera;
    private int legajo;


    public Estudiante(String nombre, int edad, String carrera, int legajo) {
        this.carrera = carrera;
        this.nombre  =  nombre;
        this.edad    =    edad;
        this.legajo  =  legajo;
    }
}
